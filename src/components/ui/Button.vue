<script setup lang="ts">
import { computed } from 'vue'

interface Props {
  variant?: 'default' | 'ghost' | 'outline' | 'destructive'
  size?: 'default' | 'sm' | 'lg' | 'icon'
  as?: string
}

const props = withDefaults(defineProps<Props>(), {
  variant: 'default',
  size: 'default',
  as: 'button'
})

const buttonClass = computed(() => {
  const base = 'inline-flex items-center justify-center rounded-md font-medium transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring disabled:pointer-events-none disabled:opacity-50 active:scale-95'

  const variants = {
    default: 'bg-primary text-primary-foreground hover:bg-primary/90 hover:shadow-md',
    ghost: 'hover:bg-accent hover:text-accent-foreground',
    outline: 'border border-input bg-background hover:bg-accent hover:text-accent-foreground hover:shadow-sm',
    destructive: 'bg-destructive text-destructive-foreground hover:bg-destructive/90 hover:shadow-md'
  }

  const sizes = {
    default: 'h-10 px-4 py-2',
    sm: 'h-9 rounded-md px-3',
    lg: 'h-11 rounded-md px-8',
    icon: 'h-10 w-10'
  }

  return `${base} ${variants[props.variant]} ${sizes[props.size]}`
})
</script>

<template>
  <component :is="as" :class="buttonClass">
    <slot />
  </component>
</template>
